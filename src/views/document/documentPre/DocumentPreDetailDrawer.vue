<template>
  <a-drawer v-model:visible="visible" title="机构报考-考生上传资料详情" :width="width >= 600 ? 600 : '100%'" :footer="false">
    <a-descriptions :column="2" size="large" class="general-description">
      <a-descriptions-item label="资料ID">{{ dataDetail?.id }}</a-descriptions-item>
      <a-descriptions-item label="机构报考-考生扫码上传文件表id">{{ dataDetail?.enrollPreUploadId }}</a-descriptions-item>
      <a-descriptions-item label="存储路径">{{ dataDetail?.docPath }}</a-descriptions-item>
      <a-descriptions-item label="关联资料类型ID">{{ dataDetail?.typeId }}</a-descriptions-item>
      <a-descriptions-item label="创建人ID">{{ dataDetail?.createUser }}</a-descriptions-item>
      <a-descriptions-item label="创建人">{{ dataDetail?.createUserString }}</a-descriptions-item>
      <a-descriptions-item label="更新人ID">{{ dataDetail?.updateUser }}</a-descriptions-item>
      <a-descriptions-item label="修改人">{{ dataDetail?.updateUserString }}</a-descriptions-item>
      <a-descriptions-item label="创建时间">{{ dataDetail?.createTime }}</a-descriptions-item>
      <a-descriptions-item label="更新时间">{{ dataDetail?.updateTime }}</a-descriptions-item>
      <a-descriptions-item label="删除标记(0未删,1已删)">{{ dataDetail?.isDeleted }}</a-descriptions-item>
    </a-descriptions>
  </a-drawer>
</template>

<script setup lang="ts">
import { useWindowSize } from '@vueuse/core'
import { type DocumentPreDetailResp, getDocumentPre as getDetail } from '@/apis/document/documentPre'

const { width } = useWindowSize()

const dataId = ref('')
const dataDetail = ref<DocumentPreDetailResp>()
const visible = ref(false)

// 查询详情
const getDataDetail = async () => {
  const { data } = await getDetail(dataId.value)
  dataDetail.value = data
}

// 打开
const onOpen = async (id: string) => {
  dataId.value = id
  await getDataDetail()
  visible.value = true
}

defineExpose({ onOpen })
</script>


<template>
  <a-drawer v-model:visible="visible" title="项目地点关联详情" :width="width >= 600 ? 600 : '100%'" :footer="false">
    <a-descriptions :column="2" size="large" class="general-description">
      <a-descriptions-item label="关联ID">{{ dataDetail?.id }}</a-descriptions-item>
      <a-descriptions-item label="项目ID">{{ dataDetail?.projectId }}</a-descriptions-item>
      <a-descriptions-item label="地点ID">{{ dataDetail?.locationId }}</a-descriptions-item>
      <a-descriptions-item label="描述">{{ dataDetail?.redeme }}</a-descriptions-item>
      <a-descriptions-item label="创建人ID">{{ dataDetail?.createUser }}</a-descriptions-item>
      <a-descriptions-item label="创建人">{{ dataDetail?.createUserString }}</a-descriptions-item>
      <a-descriptions-item label="更新人ID">{{ dataDetail?.updateUser }}</a-descriptions-item>
      <a-descriptions-item label="修改人">{{ dataDetail?.updateUserString }}</a-descriptions-item>
      <a-descriptions-item label="创建时间戳">{{ dataDetail?.createTime }}</a-descriptions-item>
      <a-descriptions-item label="更新时间戳">{{ dataDetail?.updateTime }}</a-descriptions-item>
      <a-descriptions-item label="删除标记">{{ dataDetail?.isDeleted }}</a-descriptions-item>
    </a-descriptions>
  </a-drawer>
</template>

<script setup lang="ts">
import { useWindowSize } from '@vueuse/core'
import { type ProjLocAssocDetailResp, getProjLocAssoc as getDetail } from '@/apis/exam/projLocAssoc'

const { width } = useWindowSize()

const dataId = ref('')
const dataDetail = ref<ProjLocAssocDetailResp>()
const visible = ref(false)

// 查询详情
const getDataDetail = async () => {
  const { data } = await getDetail(dataId.value)
  dataDetail.value = data
}

// 打开
const onOpen = async (id: string) => {
  dataId.value = id
  await getDataDetail()
  visible.value = true
}

defineExpose({ onOpen })
</script>

